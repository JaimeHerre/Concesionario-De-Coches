    private void atrasBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atrasBtnActionPerformed
        indice--;
        if (indice>=0) {
            mostrarCoche();
            if(indice==0) {
                atrasBtn.setEnabled(false);
            }
            if (!adelanteBtn.isEnabled()) {
                adelanteBtn.setEnabled(true);
            }
        } else {
            atrasBtn.setEnabled(false);
            indice++;
        }
    }//GEN-LAST:event_atrasBtnActionPerformed

    private void adelanteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adelanteBtnActionPerformed
        indice++;
        if (indice<Gestion.getConcesionario().size()) {
            mostrarCoche();
            if (indice==Gestion.getConcesionario().size()-1) {
                adelanteBtn.setEnabled(false);
            }
            if (!atrasBtn.isEnabled()) {
                atrasBtn.setEnabled(true);
            }
        } else {
            adelanteBtn.setEnabled(false);
            indice--;
        }
    }//GEN-LAST:event_adelanteBtnActionPerformed

