    private void mostrarCoche(){
        Coche c = Gestion.getConcesionario().get(indice);
        matriculaResLabel.setText(c.getMatricula());
        modeloResLabel.setText(c.getModelo().toString());
        colorResLabel.setText(c.getColor().toString());
        setModal(true);
}
